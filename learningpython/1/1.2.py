#ARITHMETIC OPERATORS
x = 9
y = 3.5 #Both floats and ints work
result = x/y
result = int(x/y) #converts result to int
print(result)  
x/y #division
x*y #multiplication
x+y #addition
x-y #subtraction
x%y #modulo (remainder)
x**y #exponentiation
x//y #floor division (rounds down to nearest whole number)
#Python follows Bedmas (Brackets, Exponents, Division, Multiplication, Addition, Subtraction) when calculating equations
